package com.encapsulation;

public class EncapsulationConcept 
{
	private int marks;
	private String name;
	
	
	public String getName() 
	{
		return name;
	}


	public void setName(String name) 
	{
		this.name = name;
	}


	public int getMarks() 
	{
		return marks;
	}


	public void setMarks(int marks) 
	{
		this.marks = marks;
	}
}
